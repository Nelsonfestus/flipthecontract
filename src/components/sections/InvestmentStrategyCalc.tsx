      const cashOnCashReturn = cashInvested > 0 ? annualCashFlow / cashInvested : 0

      // NOI = Net Operating Income (before debt service)
      const annualNOI = (rent * 12) - (monthlyTax * 12) - (monthlyIns * 12) - (monthlyMaint * 12) - (monthlyVacancy * 12) - (monthlyMgmt * 12)
      const capRate = pp > 0 ? annualNOI / pp : 0
      const grm = rent > 0 ? pp / (rent * 12) : 0
      const dscr = monthlyMortgage > 0 ? (annualNOI / 12) / monthlyMortgage : 0

      // 5-year projection
      const fiveYearEquity = pp * Math.pow(1 + apprPct / 100, 5) - loanAmount * 0.92 // rough principal paydown
      const fiveYearCashFlow = annualCashFlow * 5
      const fiveYearTotalReturn = fiveYearEquity - pp + fiveYearCashFlow

      let verdict = ''
