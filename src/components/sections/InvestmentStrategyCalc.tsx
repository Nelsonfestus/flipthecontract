/* ── Download helpers ─────────────────────────────── */
function downloadReport(title: string, lines: string[]) {
  const text = `${title}\nGenerated: ${new Date().toLocaleDateString()}\n\n${lines.join('\n')}\n\n---\nGenerated by Flip the Contract — Investment Strategy Calculator\nFor educational purposes only. Always verify with your own due diligence.\n© 2026 Flip the Contract. All rights reserved.`
  const blob = new Blob([text], { type: 'text/plain' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = `Investment_Analysis_${title.replace(/\s+/g, '_')}.txt`
  document.body.appendChild(a)
  a.click()
  document.body.removeChild(a)
  URL.revokeObjectURL(url)
}

